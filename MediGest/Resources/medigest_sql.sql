-- =========================
-- BASE DE DATOS MEDIGEST
-- Script completo con procedimientos y datos
-- =========================

CREATE DATABASE IF NOT EXISTS medigest;
USE medigest;

-- =========================
-- CREACIÓN DE TABLAS
-- =========================

-- USUARIO
CREATE TABLE Usuario (
    id_usuario INT AUTO_INCREMENT PRIMARY KEY,
    login VARCHAR(50) UNIQUE NOT NULL,
    password VARCHAR(100) NOT NULL,
    rol ENUM('recepcionista','medico','admin') NOT NULL
);

-- RECEPCIONISTA
CREATE TABLE Recepcionista (
    id_recepcionista INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario INT NOT NULL,
    nombre VARCHAR(100),
    apellidos VARCHAR(150),
    telefono VARCHAR(20) UNIQUE,
    email VARCHAR(100),
    FOREIGN KEY (id_usuario) REFERENCES Usuario(id_usuario)
        ON DELETE CASCADE
);

-- ESPECIALIDAD
CREATE TABLE Especialidad (
    id_especialidad INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    descripcion TEXT,
    precio DECIMAL(10,2) NOT NULL DEFAULT 0
);

-- MÉDICO
CREATE TABLE Medico (
    id_medico INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario INT NOT NULL,
    id_especialidad INT NOT NULL,
    nombre VARCHAR(100) NOT NULL,
    apellidos VARCHAR(150),
    num_colegiado VARCHAR(50) UNIQUE,
    correo_corporativo VARCHAR(100),
    FOREIGN KEY (id_usuario) REFERENCES Usuario(id_usuario)
        ON DELETE CASCADE,
    FOREIGN KEY (id_especialidad) REFERENCES Especialidad(id_especialidad)
        ON DELETE RESTRICT
);

-- PACIENTE
CREATE TABLE Paciente (
    id_paciente INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    apellidos VARCHAR(150),
    dni VARCHAR(15) UNIQUE,
    cipa VARCHAR(30),
    num_historia_clinica VARCHAR(30),
    num_seguridad_social VARCHAR(30),
    fecha_nacimiento DATE,
    fecha_ingreso DATE,
    correo VARCHAR(30)
);

-- CITA
CREATE TABLE Cita (
    id_cita INT AUTO_INCREMENT PRIMARY KEY,
    id_paciente INT NOT NULL,
    id_medico INT,
    id_recepcionista INT,
    fecha DATE NOT NULL,
    hora TIME NOT NULL,
    estado ENUM('pendiente','confirmada','cancelada','realizada') DEFAULT 'pendiente',
    observaciones TEXT,
    FOREIGN KEY (id_paciente) REFERENCES Paciente(id_paciente),
    FOREIGN KEY (id_medico) REFERENCES Medico(id_medico),
    FOREIGN KEY (id_recepcionista) REFERENCES Recepcionista(id_recepcionista)
);

-- INFORME MÉDICO
CREATE TABLE Informe_Medico (
    id_informe INT AUTO_INCREMENT PRIMARY KEY,
    id_paciente INT,
    id_medico INT,
    id_cita INT,
    fecha_emision DATE NOT NULL,
    motivo_consulta VARCHAR(255),
    diagnostico TEXT,
    tratamiento TEXT,
    observaciones TEXT,
    FOREIGN KEY (id_paciente) REFERENCES Paciente(id_paciente),
    FOREIGN KEY (id_medico) REFERENCES Medico(id_medico),
    FOREIGN KEY (id_cita) REFERENCES Cita(id_cita)
        ON DELETE CASCADE
);

-- =========================
-- PROCEDIMIENTO ALMACENADO
-- =========================

DELIMITER //

CREATE PROCEDURE InsertarUsuario(
    IN p_login VARCHAR(50),
    IN p_password VARCHAR(100),
    IN p_rol ENUM('recepcionista','medico','admin')
)
BEGIN
    -- Encripta la contraseña usando SHA2 con 256 bits
    -- SHA2-256 es un algoritmo robusto y ampliamente utilizado
    INSERT INTO Usuario (login, password, rol)
    VALUES (p_login, SHA2(p_password, 256), p_rol);
END //

